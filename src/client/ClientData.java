package client;

public class ClientData {
	private String id_client;
	private String loan_amount;
	private String dlq;
	private String dpd;
	private String currency;
	private String loan_type;
	private String id_loan;
	private String grant_date;
	private int no;
	
	public String getId_client() {
		return id_client;
	}
	public void setId_client(String id_client) {
		this.id_client = id_client;
	}
	public String getLoan_amount() {
		return loan_amount;
	}
	public void setLoan_amount(String loan_amount) {
		this.loan_amount = loan_amount;
	}
	public String getDlq() {
		return dlq;
	}
	public void setDlq(String dlq) {
		this.dlq = dlq;
	}
	public String getDpd() {
		return dpd;
	}
	public void setDpd(String dpd) {
		this.dpd = dpd;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getLoan_type() {
		return loan_type;
	}
	public void setLoan_type(String loan_type) {
		this.loan_type = loan_type;
	}
	public String getId_loan() {
		return id_loan;
	}
	public void setId_loan(String id_loan) {
		this.id_loan = id_loan;
	}
	public String getGrant_date() {
		return grant_date;
	}
	public void setGrant_date(String grant_date) {
		this.grant_date = grant_date;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	
}
